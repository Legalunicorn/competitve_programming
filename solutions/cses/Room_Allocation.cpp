void solve(){
    int n;
    cin >> n;
    vvl a(n,vl(3));
    for (int i=0;i<n;i++){
        ll l,r; 
        cin >> l  >> r;
        // a[i] = {l,r};
        a[i] = {l,r,i};
    }
    sort(all(a),[](const auto&p, const auto& q){
        if (p[0]==q[0]) return p[1]<q[1];
        return p[0]<q[0];
        // if (p.F==q.F) return p.S<q.S;
        // return p.F < q.F;
    });
    // minpqll pq;
    const auto cmp = [](const auto& p, const auto& q){
        return p.F > q.F;
    };
    priority_queue<pl,vpl,decltype(cmp)> pq(cmp);
    stack<int> avail;
    int res = 0;
    vi id(n);
    for (int i=0;i<n;i++){
        while(!pq.empty() && pq.top().F<a[i][0]) {
            avail.push(pq.top().S);
            pq.pop();
        }
        int room =0;
        if (!avail.empty()){
            room = avail.top();
            avail.pop();
        } else{ //all rooms are filled
            room = res+1;
        }
        id[a[i][2]] = room;
        res = max(res,room);
        pq.push({a[i][1],room});

    }
    cout << res << endl;
    for (int i:id) cout << i << " ";
    cout << endl;

};


