void solve(){
    int n;
    cin >> n;
    vvi g(n);
    for (int i=0;i<n-1;i++){
        int a,b;
        cin >> a >> b;
        a--;b--;
        g[a].pb(b);
        g[b].pb(a);
    }
    vb vis(n,false);
    vi depth(n,0);
    dfs(0,-1,vis,g,0,depth);
    //first the node with the max depth and repeat 
    int id = 0;
    int md = 0;
    for (int i=0;i<n;i++){
        if (depth[i]>md){
            md = depth[i];
            id = i;
        }
    }
    //perform a dfs aga in
    vis.assign(n,false);
    depth.assign(n,0);
    dfs(id,-1,vis,g,0,depth);
    int res = MAX(depth);
    cout << res << endl;


};


