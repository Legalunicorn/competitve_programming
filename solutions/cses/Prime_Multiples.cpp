void solve(){
    //Solve using PIE 
    ll n; cin >>n;
    ll k; cin >>k;
    vector<ll> primes(k);
    for (ll& p:primes) cin >> p;

    ll res = 0;
    for (ll  mask=1;mask<(1<<k);mask++){
        // ll _lcm = 1;

        ll prod = 1;
        ll bits =0;
        bool skip = false;
        for (ll i=0;i<k;i++ ){
            if ((mask>>i)&1){
                if (prod>n/primes[i]){
                    prod = n+1;
                    break;
                } else{ 
                    prod *= primes[i];
                    bits++;
                }    
            }
        }
        ll val = n/prod;
        if (bits%2==0) res -= val;
        else res += val;
    }
    cout << res << endl;

};
