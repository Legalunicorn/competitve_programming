// otherwise we need to split MOD to N 
// MOD * (2 ^ x) % N = 0 
void solve(){
    ll n,m;
    cin >> n >> m;
    n %= m;
    ll x =  n / __gcd(n,m);
    ll y = m / __gcd(n,m);
    if (__builtin_popcount(y) > 1){
        cout << -1 << endl;
    } else {
        int b = __builtin_popcount(x);
        cout << m * b - n << endl;
    }
};

